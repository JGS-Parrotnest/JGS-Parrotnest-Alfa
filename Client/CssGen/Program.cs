    static void GenerateMoreUtilities(StringBuilder sb)
    {
        // Rotation
        for (int i = 0; i <= 360; i += 5)
        {
            sb.AppendLine($".rotate-{i} {{ transform: rotate({i}deg) !important; }}");
            sb.AppendLine($".rotate-n{i} {{ transform: rotate(-{i}deg) !important; }}");
        }

        // Scale
        for (int i = 0; i <= 200; i += 5)
        {
            sb.AppendLine($".scale-{i} {{ transform: scale({i/100.0}) !important; }}");
        }

        // Translate
        for (int i = -100; i <= 100; i += 5)
        {
            sb.AppendLine($".translate-x-{i} {{ transform: translateX({i}%) !important; }}");
            sb.AppendLine($".translate-y-{i} {{ transform: translateY({i}%) !important; }}");
        }

        // Min/Max Width/Height
        for (int i = 0; i <= 100; i += 5)
        {
            sb.AppendLine($".min-w-{i}p {{ min-width: {i}% !important; }}");
            sb.AppendLine($".max-w-{i}p {{ max-width: {i}% !important; }}");
            sb.AppendLine($".min-h-{i}vh {{ min-height: {i}vh !important; }}");
            sb.AppendLine($".max-h-{i}vh {{ max-height: {i}vh !important; }}");
        }

        // Border Widths
        for (int i = 0; i <= 20; i++)
        {
            sb.AppendLine($".bw-{i} {{ border-width: {i}px !important; border-style: solid; }}");
            sb.AppendLine($".bt-{i} {{ border-top-width: {i}px !important; border-style: solid; }}");
            sb.AppendLine($".bb-{i} {{ border-bottom-width: {i}px !important; border-style: solid; }}");
            sb.AppendLine($".bl-{i} {{ border-left-width: {i}px !important; border-style: solid; }}");
            sb.AppendLine($".br-w-{i} {{ border-right-width: {i}px !important; border-style: solid; }}");
        }

        // Gap (Flex/Grid)
        for (int i = 0; i <= 60; i++)
        {
            sb.AppendLine($".gap-{i} {{ gap: {i}px !important; }}");
            sb.AppendLine($".gap-x-{i} {{ column-gap: {i}px !important; }}");
            sb.AppendLine($".gap-y-{i} {{ row-gap: {i}px !important; }}");
        }

        // Line Height
        for (int i = 10; i <= 30; i++)
        {
            sb.AppendLine($".lh-{i} {{ line-height: {i/10.0} !important; }}");
        }

        // Letter Spacing
        for (int i = -5; i <= 10; i++)
        {
            sb.AppendLine($".ls-{i} {{ letter-spacing: {i/10.0}px !important; }}");
        }
    }
